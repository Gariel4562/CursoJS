// Objeto Math, uma função dentro de um objeto chama-se Metodo (method). ** é operador de elevação. 100 / 0 da resultado infinity, em outras linguagens da erro. porém ele diz que a conta deu certo, mesmo tendo resultado infinity. então seria True, verdadeiro, a conta deu certo.

let num1 = 9.454665;

let num2 = Math.round(num1); // Estou arredondando para baixo o valor do num1. ou seja.. o resultado será 9 pois, o restante será ignorado. "ceil" no lugar de "floor" arredonda para cima. "round" no lugar de "ceil" e "floor" arredonda para o numero mais próximo no caso seria 9.

console.log(num2);

console.log(Math.max(1, 2, 3, 5, 4, 6, 8 ,9, 1000, 300)); // vai retornar o maior numero. se quiser o menor será "min" no lugar de "max"

console.log(Math.random()); // vai gerar vários numeros aleatórios.

console.log(Math.PI); // vai achar o valor de Pi

console.log(num1 ** 0.5); // vai achar a raiz quadrada do num1. 

