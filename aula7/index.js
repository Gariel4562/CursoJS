// Conta básica com Const, porém não posso alterar esses valores mais pra frente. o valor que defini inicialmente continuará até o fim.

const primeiroNumero = 9;
const segundoNumero = 10;

resultado = primeiroNumero + segundoNumero;

const resultadoDuplicado = resultado * 2

// se declarar a variável com Let eu posso mudar o valor dela.

let resultadoTriplicado = resultadoDuplicado * 3;
resultadoTriplicado = resultadoTriplicado + 15;


console.log(`${resultado}`);
console.log(`${resultadoDuplicado}`);
console.log(`${resultadoTriplicado}`);

