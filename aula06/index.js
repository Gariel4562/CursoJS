// Variável algo que pode ser modificado ao decorrer do código. 

let nome1 = 'João';
let nome2 = 'Maria';
let nome3 = 'Eduardo';


console.log(`${nome1} nasceu em 1984.`);
console.log(`Em 2000 ${nome1} conheceu ${nome2}. `);
console.log(`Casou-se com ${nome2} em 2012.`);
console.log(`${nome2} teve um filho com ${nome1} em 2015.`);
console.log(`O filho de ${nome1} se chama ${nome3}. `);

